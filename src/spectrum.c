INT8U save_dump_data( INT8U spectrum_type, INT32U sample_size )
{
	int fd;
	char file_name[100] = {0};
	char file_type[10] = {0};
	char sub_string[80] = {0};
	char time_buf[30] = {0};
	INT32U cnt = 0;
	time_t tm_time;
	struct tm *st_time;

//	printf("YCSO CHECK POINT [save_dump_data]\n");
	switch( spectrum_type )
	{
		case _DL0_DUMP:
		case _DL1_DUMP:
		case _DL2_DUMP:
		case _DL3_DUMP:
			snprintf( file_type, sizeof( file_type ), "DL%d", spectrum_type&0x0f);
			break;

		case _UL0_DUMP:
		case _UL1_DUMP:
		case _UL2_DUMP:
		case _UL3_DUMP:
			snprintf( file_type, sizeof( file_type ), "UL%d", spectrum_type&0x0f);
			break;

		case _UL0_NOISE_DUMP:
		case _UL1_NOISE_DUMP:
		case _UL2_NOISE_DUMP:
		case _UL3_NOISE_DUMP:
			snprintf( file_type, sizeof( file_type ), "UL_NOISE%d", spectrum_type&0x0f);
			break;

		default:
			return 1;
	}

	time( &tm_time);
	st_time = localtime( &tm_time);
	strftime( time_buf, sizeof(time_buf), "%Y%m%d_%I%M%S", st_time);

	snprintf( file_name, sizeof( file_name ), "/tmp/dump_%s_%s", time_buf, file_type);
	
	fd = open( file_name, O_CREAT|O_WRONLY|O_SYNC, 0644 );

	if( fd < 0 )
	{
		close(fd);
		return 2;
	}

	for( cnt = 0; cnt < sample_size; cnt++ )
	{
		snprintf( sub_string, sizeof(sub_string), "%4d  %6d  %6d\n", cnt ,  sR[cnt], sI[cnt]);
		write(fd, sub_string, strlen(sub_string));
	}

	close(fd);

	printf(" make dump file %s \n", file_name );

	return 0;
	
}


