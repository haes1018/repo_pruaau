static void signal_normaization(float* uR, float* uI, INT32U sample_size )
{
	INT16U i;

	for( i = 0; i < sample_size; i++)
	{
		uR[i] = S0_DEF * (float)sR[i];
		uI[i] = S0_DEF * (float)sI[i];
	}
}


static void fft_table( INT32U sample_size)
{
	int i, n_half, ne, jp;
	float arg;

	arg = M_PI_X2/sample_size;

	for( i = 0; i < (( sample_size*3 ) >> 2 ); i++)
		wn_FFT[i] = cos(arg*i);

	n_half = sample_size>>1;
	br_FFT[0] = 0;
	for( ne = 1; ne < sample_size; ne = ne<<1)
	{
		for( jp = 0; jp < ne; jp++)
			br_FFT[jp+ne] = br_FFT[jp]+n_half;
		n_half = n_half >> 1;
	}
}

static void hamming_window( float* uR, float* uI, float* wR, float* wI, INT32U sample_size)
{
	INT16U i;
	float wM, pi2N;

	pi2N = M_PI_X2 / (float)sample_size;

	for( i = 0; i < sample_size; i++ )
	{
		wM = ( 0.54 - ( 0.46 * cos(pi2N*i)));
		wR[i] = uR[i] * wM;
		wI[i] = uI[i] * wM;

	}


}

static void swap(float *a, float *b)
{
	float tmp;
	tmp = *a;
	*a = *b;
	*b = tmp;
}

static void fft( float *xR, float *xI, INT32U sample_size)
{
	float xtmpR, xtmpI;
	int j, jnh, k, jxC, jxS, ne, n_half, n_half2;

	n_half = sample_size>>1;

	for( ne=1; ne<sample_size ; ne=ne<<1 )
	{
		n_half2 = n_half<<1;

		for( k=0 ; k<sample_size ; k=k+n_half2 )
		{
			jxC = 0;
			jxS = sample_size>>2;

			for( j=k ; j<(k+n_half) ; j++)
			{
				jnh = j + n_half;
				// Beginning of Butterfly operation
				xtmpR = xR[j];
				xtmpI = xI[j];
				xR[j] = xtmpR + xR[jnh];
				xI[j] = xtmpI + xI[jnh];
				xtmpR = xtmpR - xR[jnh];
				xtmpI = xtmpI - xI[jnh];
				xR[jnh] = ( xtmpR*wn_FFT[jxC] ) - ( xtmpI*wn_FFT[jxS] );
				xI[jnh] = ( xtmpR*wn_FFT[jxS] ) + ( xtmpI*wn_FFT[jxC] );

				// end of butterfly operations
				jxC = jxC + ne;
				jxS = jxS + ne;
			}
		}
		n_half = n_half >> 1;
	}

	// Bit reverse
	for( j = 0; j< sample_size; j++)
	{
		if( j<br_FFT[j] )
		{
			swap( &xR[j], &xR[br_FFT[j]] );
			swap( &xI[j], &xI[br_FFT[j]] );
		}
	}
}

void Spectrum_calc( float* xP ,INT16U sample_size )
{
	INT16U i = 0;

	float *xR = NULL;
	float *xI = NULL;

	xR = (float*)malloc( ( sizeof(float) )*FFT_8192);
	xI = (float*)malloc( ( sizeof(float) )*FFT_8192);
//	float xR[FFT_8192] = {0};
//	float xI[FFT_8192] = {0};

	memset(xR,0,sizeof(float)*FFT_8192);
	memset(xI,0,sizeof(float)*FFT_8192);

	fft_table( sample_size );
	signal_normaization( xR, xI, sample_size);

	hamming_window(xR, xI, xR, xI, sample_size);

	fft(xR, xI, sample_size);

	for( i = 0; i < sample_size; i++ )
		xP[i] = (xR[i]*xR[i]) + (xI[i]*xI[i]);

	for( i = 0; i < sample_size; i++ )
		xP[i] = (xP[i] == 0 ) ? -100.0 : 10.0*log10(xP[i]);

	free(xR);
	free(xI);
}

