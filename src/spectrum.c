int sigmon_span_converter(int a_iStartOffset, double a_fCoeff, float* a_psSrc, float* a_psDst, int a_iTotalPoint)
{
    double fNext = 0;
    int iNext;
    int newIndex = 0;

    fNext += a_fCoeff;
    iNext = (int)fNext;
    for(int iIndex=0;iIndex < a_iTotalPoint;)
    {
        if(iIndex == iNext)
        {
            a_psDst[newIndex++] = (short)((a_psSrc[a_iStartOffset + iIndex - 1]+a_psSrc[a_iStartOffset+iIndex])/2.0);
            fNext += a_fCoeff;
            iNext = (int)fNext;
        }
        else
        {
            a_psDst[newIndex++] = a_psSrc[a_iStartOffset+(iIndex++)];
        }
    }

    return 0;
}


